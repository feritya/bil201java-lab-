public class Bug {                     
    //Bug sınıfını oluşturduk
    private String character;

    public Bug(String character) { 
        // yapıcı metodumuzu oluşturduk
        this.character = character;
    }

    public String toString() {
        //metodu ile birlikte gelecek şekilde bug karakterlerini temsil etmek için bir String alanı tanımladık
        return character;
    }

}
